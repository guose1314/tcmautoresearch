class TCMLexicon:
    """中医术语词典，支持按类型查询和外部词典扩展。"""

    def __init__(self) -> None:
        self._words: Dict[str, Dict[str, Any]] = {}
        self._type_index: Dict[str, Set[str]] = {}
        self._load_builtin()

    # ------------------------------------------------------------------
    # 公共 API
    # ------------------------------------------------------------------

    def get_vocab_size(self) -> int:
        """返回词典中的总词汇数。"""
        return len(self._words)

    def get_all_words(self) -> List[str]:
        """返回所有词汇列表。"""
        return list(self._words.keys())

    def get_words_by_type(self, word_type: str) -> List[str]:
        """返回指定类型的词汇列表。"""
        return list(self._type_index.get(word_type, set()))

    def get_word_info(self, word: str) -> Optional[Dict[str, Any]]:
        """返回指定词的元数据；不存在返回 None。"""
        return self._words.get(word)

    def contains(self, word: str) -> bool:
        """判断词汇是否存在于词典中。"""
        return word in self._words

    def get_types(self) -> List[str]:
        """返回所有词汇类型列表。"""
        return list(self._type_index.keys())

    def add_word(
        self,
        word: str,
        word_type: str = "common",
        metadata: Optional[Dict[str, Any]] = None,
    ) -> None:
        """添加单个词汇。"""
        if not word or not word.strip():
            return
        word = word.strip()
        entry = {"type": word_type, **(metadata or {})}
        self._words[word] = entry
        self._type_index.setdefault(word_type, set()).add(word)

    def load_from_file(
        self,
        path: str,
        word_type: str = "common",
        encoding: str = "utf-8",
    ) -> int:
        """
        从外部文件加载词汇。

        支持格式：
        - .json: ``{"words": ["word1", ...]}`` 或 ``["word1", ...]``
        - .txt / 其他: 每行一个词，可选 tab 分隔 ``word\\tfreq``

        Returns:
            新增词汇数量。
        """
        file_path = Path(path)
        if not file_path.exists():
            logger.warning("词典文件不存在: %s", path)
            return 0

        count_before = len(self._words)
        suffix = file_path.suffix.lower()

        if suffix == ".json":
            self._load_json_file(file_path, word_type, encoding)
        else:
            self._load_text_file(file_path, word_type, encoding)

        added = len(self._words) - count_before
        logger.info("从 %s 加载 %d 个新词汇 (类型: %s)", path, added, word_type)
        return added

