class TCMLexicon:
    """中医术语词典 — 支持 6000+ 词汇及外部扩展"""

    def __init__(self) -> None:
        # 按类型存储词汇（使用 dict 保持唯一性并记录类型）
        self.herbs: Dict[str, str] = {w: "herb" for w in _HERBS}
        self.formulas: Dict[str, str] = {w: "formula" for w in _FORMULAS}
        self.syndromes: Dict[str, str] = {w: "syndrome" for w in _SYNDROMES}
        self.theory: Dict[str, str] = {w: "theory" for w in _THEORY}
        self.efficacy: Dict[str, str] = {w: "efficacy" for w in _EFFICACY}
        self.common_words: Dict[str, str] = {w: "common" for w in _COMMON}

        # 全局词汇索引：word → type，合并所有子词典
        self._index: Dict[str, str] = {}
        for sub in (self.herbs, self.formulas, self.syndromes,
                    self.theory, self.efficacy, self.common_words):
            self._index.update(sub)

        logger.info(f"TCMLexicon 初始化完成，词汇总数: {len(self._index):,}")

    # ------------------------------------------------------------------
    # 查询接口
    # ------------------------------------------------------------------

    def get_vocab_size(self) -> int:
        """返回词典中词汇总数"""
        return len(self._index)

    def get_all_words(self) -> List[str]:
        """返回所有词汇列表"""
        return list(self._index.keys())

    def get_word_type(self, word: str) -> Optional[str]:
        """返回词汇对应的类型，不存在时返回 None"""
        return self._index.get(word)

    def contains(self, word: str) -> bool:
        """检查词汇是否存在于词典中"""
        return word in self._index

    # ------------------------------------------------------------------
    # 扩展接口
    # ------------------------------------------------------------------

    def add_word(self, word: str, word_type: str = "common") -> None:
        """向词典中添加单个词汇"""
        if not word or not isinstance(word, str):
            return
        self._index[word] = word_type
        sub = self._get_sub_dict(word_type)
        sub[word] = word_type

    def load_from_file(self, filepath: str, word_type: str = "common") -> int:
        """
        从文件加载外部词典（支持 THUOCL 等格式）。

        支持的格式：
          - 每行一个词条（词汇 [频率]），以空白分隔，取第一个字段
          - 空行和 # 开头的注释行会被跳过

        Args:
            filepath: 词典文件路径
            word_type: 词汇分类

        Returns:
            成功加载的词汇数量
        """
        path = Path(filepath)
        if not path.exists():
            logger.warning(f"外部词典文件不存在: {filepath}")
            return 0

        count = 0
        try:
            with path.open(encoding="utf-8", errors="replace") as fh:
                for raw_line in fh:
                    line = raw_line.strip()
                    if not line or line.startswith("#"):
                        continue
                    word = line.split()[0]
                    if word and word not in self._index:
                        self.add_word(word, word_type)
                        count += 1
            logger.info(f"从 {filepath} 加载 {count} 个 {word_type} 词汇")
        except Exception as exc:
            logger.error(f"加载词典文件失败 {filepath}: {exc}")

        return count

    def export_to_jieba_format(self, filepath: str,
                                word_type: str = "common") -> None:
        """
        将词典导出为 jieba 用户词典格式（每行：词 频率 词性）。

        Args:
            filepath: 输出文件路径
            word_type: 仅导出该类型的词汇；传入 "all" 则导出全部
        """
        path = Path(filepath)
        path.parent.mkdir(parents=True, exist_ok=True)
        try:
            with path.open("w", encoding="utf-8") as fh:
                for word, wtype in self._index.items():
                    if word_type == "all" or wtype == word_type:
                        fh.write(f"{word} 100 n\n")
            logger.info(f"词典已导出到 {filepath}")
        except Exception as exc:
            logger.error(f"导出词典失败: {exc}")

    # ------------------------------------------------------------------
    # 内部辅助
    # ------------------------------------------------------------------

    def _get_sub_dict(self, word_type: str) -> Dict[str, str]:
        mapping = {
            "herb": self.herbs,
            "formula": self.formulas,
            "syndrome": self.syndromes,
            "theory": self.theory,
            "efficacy": self.efficacy,
        }
        return mapping.get(word_type, self.common_words)


# ---------------------------------------------------------------------------
# 全局单例
# ---------------------------------------------------------------------------

_lexicon_instance: Optional[TCMLexicon] = None


def get_lexicon() -> TCMLexicon:
    """返回全局 TCMLexicon 单例（懒初始化）"""
    global _lexicon_instance
    if _lexicon_instance is None:
        _lexicon_instance = TCMLexicon()
    return _lexicon_instance
