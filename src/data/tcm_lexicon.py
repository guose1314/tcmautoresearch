class TCMLexicon:
    """TCM 中医词典 — 支持 6000+ 词汇，可扩展至 3 万+。"""

    def __init__(self) -> None:
        # word -> type 映射
        self._words: Dict[str, str] = {}
        self._load_builtin()

    # ------------------------------------------------------------------
    def _load_builtin(self) -> None:
        for w in _BUILTIN_HERBS:
            self._words.setdefault(w, "herb")
        for w in _BUILTIN_FORMULAS:
            self._words.setdefault(w, "formula")
        for w in _BUILTIN_SYNDROMES:
            self._words.setdefault(w, "syndrome")
        for w in _BUILTIN_THEORIES:
            self._words.setdefault(w, "theory")
        for w in _BUILTIN_EFFICACIES:
            self._words.setdefault(w, "efficacy")
        logger.info("TCM 内置词典加载完成，词汇数: %d", len(self._words))

    # ------------------------------------------------------------------
    # 公共接口
    # ------------------------------------------------------------------
    def get_all_words(self) -> List[str]:
        """返回所有词汇列表。"""
        return list(self._words.keys())

    def get_word_type(self, word: str) -> Optional[str]:
        """查询词汇类型，不存在则返回 None。"""
        return self._words.get(word)

    def get_vocab_size(self) -> int:
        return len(self._words)

    def get_words_by_type(self, word_type: str) -> List[str]:
        return [w for w, t in self._words.items() if t == word_type]

    def add_word(self, word: str, word_type: str = "common") -> None:
        self._words[word] = word_type

    def load_from_file(self, path: str, word_type: str = "common") -> int:
        """
        从外部词典文件加载，每行一个词（可选 ``词\\t类型`` 格式）。
        返回新增词汇数。
        """
        added = 0
        try:
            p = Path(path)
            if not p.exists():
                logger.warning("词典文件不存在: %s", path)
                return 0
            with p.open("r", encoding="utf-8") as fh:
                for line in fh:
                    line = line.strip()
                    if not line or line.startswith("#"):
                        continue
                    parts = line.split("\t", 1)
                    w = parts[0].strip()
                    t = parts[1].strip() if len(parts) > 1 else word_type
                    if w and w not in self._words:
                        self._words[w] = t
                        added += 1
            logger.info("外部词典 %s 加载完成，新增 %d 词", path, added)
        except Exception as exc:
            logger.error("加载外部词典失败 %s: %s", path, exc)
        return added


# ---------------------------------------------------------------------------
# 全局单例
# ---------------------------------------------------------------------------
_lexicon_instance: Optional[TCMLexicon] = None


def get_lexicon() -> TCMLexicon:
    """获取全局 TCMLexicon 单例。"""
    global _lexicon_instance
    if _lexicon_instance is None:
        _lexicon_instance = TCMLexicon()
    return _lexicon_instance
